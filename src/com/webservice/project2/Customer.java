package com.webservice.project2;

public class Customer {
	private int Customer_ID;
	private String Customer_Name;
	private String Customer_Address;
	private int Customer_Zipcode;
	private String Customer_City;
	private String Customer_Country;
	private int Customer_Cellphone;
	private int Customer_Credentials;
	/**
	 * @return the customer_ID
	 */
	public int getCustomer_ID() {
		return Customer_ID;
	}
	/**
	 * @param customer_ID the customer_ID to set
	 */
	public void setCustomer_ID(int customer_ID) {
		Customer_ID = customer_ID;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return Customer_Name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		Customer_Name = name;
	}
	/**
	 * @return the customer_Address
	 */
	public String getCustomer_Address() {
		return Customer_Address;
	}
	/**
	 * @param customer_Address the customer_Address to set
	 */
	public void setCustomer_Address(String customer_Address) {
		Customer_Address = customer_Address;
	}
	/**
	 * @return the customer_Zipcode
	 */
	public int getCustomer_Zipcode() {
		return Customer_Zipcode;
	}
	/**
	 * @param customer_Zipcode the customer_Zipcode to set
	 */
	public void setCustomer_Zipcode(int customer_Zipcode) {
		Customer_Zipcode = customer_Zipcode;
	}
	/**
	 * @return the customer_City
	 */
	public String getCustomer_City() {
		return Customer_City;
	}
	/**
	 * @param customer_City the customer_City to set
	 */
	public void setCustomer_City(String customer_City) {
		Customer_City = customer_City;
	}
	/**
	 * @return the customer_Country
	 */
	public String getCustomer_Country() {
		return Customer_Country;
	}
	/**
	 * @param customer_Country the customer_Country to set
	 */
	public void setCustomer_Country(String customer_Country) {
		Customer_Country = customer_Country;
	}
	/**
	 * @return the customer_Cellphone
	 */
	public int getCustomer_Cellphone() {
		return Customer_Cellphone;
	}
	/**
	 * @param customer_Cellphone the customer_Cellphone to set
	 */
	public void setCustomer_Cellphone(int customer_Cellphone) {
		Customer_Cellphone = customer_Cellphone;
	}
	/**
	 * @return the customer_Credentials
	 */
	public int getCustomer_Credentials() {
		return Customer_Credentials;
	}
	/**
	 * @param customer_Credentials the customer_Credentials to set
	 */
	public void setCustomer_Credentials(int customer_Credentials) {
		Customer_Credentials = customer_Credentials;
	}

}
